package Week_1_BASICS.Assignment;

public class Ques3 {
    public static void main(String[] args) {
        int a = 9, b = 10;
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a-b);
        System.out.println(a%b);
        System.out.println();

        float c = 9.0f, d = 120f;
        System.out.println(c*d);
        System.out.println(c+d);
        System.out.println(c-d);
        System.out.println(c/d);
        System.out.println(c%d);
        System.out.println();

        double e = 12.0, f = 6.0;
        System.out.println(e+f);
        System.out.println(e-f);
        System.out.println(e*f);
        System.out.println(e/f);
        System.out.println(e%f);


    }
}
